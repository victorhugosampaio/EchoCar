int display_init(void) {
    if (display == NULL) {
        LOG_ERR("device pointer is NULL");
        return -EFAULT;
    }

    if (!device_is_ready(display) ) {
        LOG_ERR("display device is not ready");
        return -EBUSY;
    }

    clr_display_buf();

    return 0;
}

void add_string(char *str) {
    uint8_t idx = 0;

    for (int pos = 0; str[pos] != '\0'; pos++) {
        if (str[pos] == '\n') {
            buf_idx += 5 * (STRING_MAX_WIDTH - idx) + 3;
            idx = 0;
            continue;
        }

        if (idx >= STRING_MAX_WIDTH) {
            continue;
        }

        add_char(str[pos]);
        idx++;
    }
}

int display_print(void) {
    if (display_write(display, 0, 0, &display_buf_desc, display_buf) != 0) {
        LOG_ERR("could not write to display");
        return -EBUSY;
    }

   clr_display_buf();

    return 0;
}

static void add_char(char c) {
    uint8_t index = c - ' ';
    uint16_t start = index * 5;

    if (buf_idx >= DISPLAY_MAX_BUFFER_SIZE) {
        return;
    }

    for (int i = 0; i < 5; i++) {
        display_buf[buf_idx] = font[start + i];
        buf_idx++;
    }
}

static void clr_display_buf(void) {
     buf_idx = 0;

    for (uint16_t i = 0; i < DISPLAY_MAX_BUFFER_SIZE; i++) {
        display_buf[i] = 0x00;
    }
}